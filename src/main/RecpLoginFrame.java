    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username=userName.getText();
        String password=userPassword.getText();
        FileRead obj = new FileRead();
        List<Receptionist> list = new ArrayList<Receptionist>();
        list = obj.fileReadReceptionist();
        int i = -1;

        for (Receptionist a : list) {
            i++;
            if (username.equalsIgnoreCase(a.getUsername()) && password.equalsIgnoreCase(a.getPassword())) {
                c = 1;
                 break;
            }
        }

        if (c == 1) {
            NumberWrite num = new NumberWrite();
            num.write(i);
            RecpPanelAppointment m = new RecpPanelAppointment();
            m.setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Wrong Username or Password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

