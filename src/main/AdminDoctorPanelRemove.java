    private void jRemoveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRemoveButtonActionPerformed
        if (c == 0) {
            JOptionPane.showMessageDialog(null, "Please Click Find Button",
                    "ERROR", JOptionPane.WARNING_MESSAGE);
        } else {
            list.remove(n);
            FileRemove fr = new FileRemove();
            fr.fileRemoveDoctor(list);
            clear();

        }
    }//GEN-LAST:event_jRemoveButtonActionPerformed

    private void jFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFindActionPerformed
        String find = jRemoveName.getText();
        FileRead fw = new FileRead();
        clear();
        list = fw.fileReadDoctor();
        int i = -1;
        c = 0;
        for (Doctor dc : list) {
            i++;
            if (find.equalsIgnoreCase(dc.getName())) {
                c = 1;
                break;
            }

        }
        if (c == 0) {
            JOptionPane.showMessageDialog(null, "No Data Found",
                    "ERROR", JOptionPane.WARNING_MESSAGE);
            clear();

        } else {
            jFullName.setText(list.get(i).getName());
            jAge.setText(list.get(i).getAge());
            jGnderCombo.setSelectedItem(list.get(i).getGender());
            jAddress.setText(list.get(i).getAddress());
            jMatitalCombo.setSelectedItem(list.get(i).getMaritial());
            jPhoneNo.setText(list.get(i).getPhone());
            jQualification.setText(list.get(i).getQualification());
            jSpecialization.setText(list.get(i).getSpecialization());
            jBloodGroup.setSelectedItem(list.get(i).getBlood());
            jJoinningDate.setText(list.get(i).getJoinningDate());
            jLeavingDate.setText(list.get(i).getLeavingDate());
            n = i;
        }
    }//GEN-LAST:event_jFindActionPerformed
