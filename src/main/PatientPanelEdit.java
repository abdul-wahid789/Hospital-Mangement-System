    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jUsername.getText().isEmpty()
                || jPassword.getText().isEmpty()
                || jFullName.getText().isEmpty()
                || jAge.getText().isEmpty()
                || jGender.getSelectedItem().toString().equalsIgnoreCase("select")
                || jAddress.getText().isEmpty()
                || jMarried.getSelectedItem().toString().equalsIgnoreCase("select")
                || jPhone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Data Field is Empty",
                    "ERROR", JOptionPane.WARNING_MESSAGE);

        } else {

            String username = jUsername.getText();
            String password = jPassword.getText();
            String name = jFullName.getText();
            String age = jAge.getText();
            String gender = jGender.getSelectedItem().toString();
            String address = jAddress.getText();
            String maritial = jMarried.getSelectedItem().toString();
            String phone = jPhone.getText();

            List<Patient> chlist = new ArrayList<Patient>();
            FileRead objch = new FileRead();
            chlist = objch.fileReadPatient();
            int check = 0;
            for (Patient a : chlist) {

                if (username.equalsIgnoreCase(a.getUsername()) && !username.equalsIgnoreCase(chlist.get(patientListNumber).getUsername())) {
                    check = 1;
                }
            }

            if (check == 1) {
                JOptionPane.showMessageDialog(null, "Use Another Username");
            } else {
                Patient obj = new Patient(list.get(patientListNumber).getPatientType(),
                        list.get(patientListNumber).getDate(),
                        list.get(patientListNumber).getDisease(),
                        list.get(patientListNumber).getMedicineName(),
                        list.get(patientListNumber).getDose(),
                        list.get(patientListNumber).getTime(),
                        list.get(patientListNumber).getDiscription(),null,
                        name, age, gender, address, maritial, phone, list.get(patientListNumber).getBlood(), username, password);

                FileUpdate fu = new FileUpdate();
                fu.fileUpdatePatient(patientListNumber, obj);
                JOptionPane.showMessageDialog(null, "Updated Successfully",
                        "Success", JOptionPane.WARNING_MESSAGE);
            
                patientListNumber = -1;
             
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

